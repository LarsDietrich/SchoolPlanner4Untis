package edu.htl3r.schoolplanner.backend.schoolObjects;

import java.util.Calendar;

import edu.htl3r.schoolplanner.CalendarUtils;


public class SchoolHoliday implements SchoolObject{

	private int id;
	private String name;
	private String longName;
	private Calendar startDate;
	private Calendar endDate;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLongName() {
		return longName;
	}
	public void setLongName(String longName) {
		this.longName = longName;
	}
	public Calendar getStartDate() {
		return startDate;
	}
	public void setStartDate(Calendar startDate) {
		this.startDate = startDate;
	}
	public Calendar getEndDate() {
		return endDate;
	}
	public void setEndDate(Calendar endDate) {
		this.endDate = endDate;
	}
	
	@Override
	public String toString() {
		return "Name: " +name+"\n"
		+"Longname: " +longName+"\n"
		+"Start: " +CalendarUtils.dateToStr(startDate)+"\n"
		+"End: " +CalendarUtils.dateToStr(endDate);
	}
}